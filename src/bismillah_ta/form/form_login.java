    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
           koneksi konek = new koneksi();
           stat = (Statement)konek.GetConnection().createStatement();
           sql = "Select*from user where username='"+username.getText()+"'";
           result = stat.executeQuery(sql);
           if(result.next())
           {
               if(password.getText().equals(result.getString("password")))
               {
                   new form_utama().show();
                   this.dispose();
               }
               else
               {
                   JOptionPane.showMessageDialog(rootPane, "Password salah");
                   password.setText("");
                   username.requestFocus();
               }
           }
           else
           {
               JOptionPane.showMessageDialog(rootPane, "Username tidak ditemukan");
               username.setText("");
               password.setText("");
               username.requestFocus();
           }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "gagal");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

